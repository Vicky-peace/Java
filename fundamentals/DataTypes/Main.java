package fundamentals.DataTypes;

class Hello {
    public static void main(String a[]) {
        float marks = 95.5f;
        System.out.println("Marks: " + marks);

    }
}

// Literals
class Literals {
    public static void main(String a[]) {
        int myNum = 15; // Integer literal
        double myDouble = 5.99; // Floating-point literal
        char myLetter = 'D'; // Character literal
        boolean myBool = true; // Boolean literal
        String myText = "Hello"; // String literal

        System.out.println("Integer: " + myNum);
        System.out.println("Double: " + myDouble);
        System.out.println("Character: " + myLetter);
        System.out.println("Boolean: " + myBool);
        System.out.println("String: " + myText);
    }
}
